         <h2>Future</h2>

	<div class="inner">
	
<p>We have been at the heart of the community since 1960, serving the community from our current home on Wornington Road.</p>

<p>Our neighbourhood is currently being regenerated and our buildings and playground, owned by the Council and leased to us, are due to be demolished. We anticipate that this will happen in 2019/2020.</p>

<p>The regeneration plans do include a new facility for the community although at this stage we do not know what the new facility will look like or who will manage its services.</p>

<p>We want to continue serving the local community and are aiming to be the anchor tenant in the new facility. We also want to ensure that local people decide what facilities and services they want.</p>

<p>In order to achieve this we have started asking local people and organisations; What would you like to see in your new community facility?</p>

<p>So far the people that we have spoken to have said that facilities for children and affordable opportunities for people to come together, learn and have fun are their main priorities. More specifically lots of people have told us that they do not want to lose the adventure playground, want a theatre/music space and proper changing rooms for activities!</p>

	<p>The more people we hear from the better, if you would like to talk about this please call the centre on <a href="tel://020-8960-3234">020 8960 3234</a> or email <a href="mailto:naami@venturecentre.org.uk">naami@venturecentre.org.uk</a>.</p>

