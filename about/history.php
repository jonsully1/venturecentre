			<div class="inner">

				<p>The first adventure playground seems to have been built in Copenhagen in 1943 by landscape architect, C T Sorenson. He noticed the propensity of children left to their own devices to avoid purpose built playgrounds and resort to building sites and waste ground.
				<p>He thought that by making waste building materials available, children could have a playground with an element of risk without being life threatening. In pragmatic Scandinavian fashion he showed that this was also a way to reduce vandalism and other forms of juvenile delinquency.</p>
				<p>Notting Hill Adventure playground was originally an old church that was bombed in World War Two. Children took over the space and created various structures and swings out of the debris and various materials left behind in the wreckage. A year later Pat Smythe came and supervised the children to help create a safe space to play.</p>
				<p>The playground grew from there and 10 years later the community centre was built to complement the playground. The Notting hill adventure Playground is the oldest of its kind in the country, it has been serving the community since 1960.</p>

				
