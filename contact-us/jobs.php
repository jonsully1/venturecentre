		<h2>Children’s Services Manager</h2>
		
		<p>We are looking for an outstanding manager to guide the country’s oldest adventure playground into an exciting period of growth and development. Over 500 local children use our children’s services which include; adventure play, sports, arts and life skills. We want to create a magical environment, providing children with positive memories that will last a lifetime.</p>
		
		<p>We are looking for someone with a passion for working with children and young

people to manage this programme, someone who will support each and every child 

to reach their full potential.</p>
		
		<p>You will be able to demonstrate strong project management and development skills

as well as the ability manage and motivate staff and volunteers.</p>
		
		<p>You will possess a level 3 Play Work qualification (or equivalent) and have at least 

two years’ management experience.</p>

	<p>The salary is £30,000 for a 35 hour week (hours are flexible but the ability to work

evenings and weekends is required).</p>
		
		<p>We are looking for people who wish to gain experience in teaching or working with children.</p>

		<p>We require somebody who is able to help children complete their homework and has GSCE grades A* - C.</p>
		
		<p>To apply for this role send your CV and a cover letter to

<a href="mailto:naami@venturecentre.org.uk">naami@venturecentre.org.uk</a>. Your cover letter must detail why you are the best 

candidate for this role and how you meet the criteria outlined in the person 

specification. Applications without a cover letter will not be considered.</p>
		
		<p>The closing date for applications is <em>5pm on Friday the 18th December 2015</em>.

Interviews will take place on the week beginning the 4th January 2016.</p>

		<p>To arrange an informal chat, please contact the VCA Director, Naami Padi, on 020

8960 3234 or email at <a href="mailto:naami@venturecentre.org.uk">naami@venturecentre.org.uk</a>.</p>
	
